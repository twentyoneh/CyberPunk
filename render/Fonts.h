#pragma once
#include "raylib.h"

// Загрузка шрифта с поддержкой кириллицы.
//
// Проблема: DrawText() всегда рисует встроенным шрифтом raylib (GetFontDefault()),
// а в нём "зашиты" только базовые латинские символы — русских букв там физически
// нет, поэтому вместо них раньше рисовались "?" / пустые прямоугольники.
// Решение: подключить свой .ttf-файл (см. resources/Fonts) через LoadFontEx,
// явно указав, какие юникод-символы должны попасть в шрифт.
namespace Fonts
{
    // fontSize — размер, под который строится растровый атлас глифов. Рисовать
    // текст этим шрифтом лучше именно этим размером (см. DrawTextEx в main.cpp) —
    // иначе при масштабировании буквы будут выглядеть смазанными.
    Font LoadCyrillicFont(const char* path, int fontSize);
}
