#pragma once

// Простейший счётчик ходов для пошаговой боевой системы.
// Сейчас "ход" наступает просто по факту успешного шага игрока (см. main.cpp) —
// никакой очереди инициативы или действий противников ещё нет.
//
// Позже сюда добавится очередь инициативы (REF-based, как в правилах Cyberpunk RED):
// например, список актёров, отсортированный по инициативе, и метод, который
// передаёт ход следующему в очереди вместо простого increment.
class TurnManager
{
public:
    int GetTurnNumber() const { return turnNumber; }

    // Вызывается, когда игрок завершил своё действие за ход (сейчас — сделал шаг).
    void EndPlayerTurn()
    {
        turnNumber++;
        // TODO: здесь будут ходить NPC/противники, прежде чем управление
        // снова вернётся к игроку.
    }

private:
    int turnNumber = 1;
};
