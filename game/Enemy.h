#pragma once
#include "../character/Character.h"

// Враг на карте: позиция + полноценный Character (статы/навыки/инвентарь —
// та же модель, что и у игрока, см. Character.h). Поэтому бой с врагом
// считается тем же CombatMath::ResolveAttack, что и в /tmp-тестах для игрока —
// никакого отдельного "вражеского" урона/брони не потребовалось.
struct Enemy
{
    int gx;
    int gy;
    Character character;

    // true после победы игрока в бою с этим врагом — используется вместо
    // удаления из вектора (см. предупреждение в EnemyManager.h про
    // стабильность индексов/указателей). Побеждённый враг не блокирует
    // клетку и не может быть вовлечён в новый бой.
    bool defeated = false;
};
