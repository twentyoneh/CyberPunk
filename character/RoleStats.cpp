std::string RoleStats::roleName(Role role) {
    switch (role) {
        case Role::Rockerboy: return "Рокербой";
        case Role::Solo:      return "Соло";
        case Role::Netrunner: return "Нетраннер";
        case Role::Tech:      return "Техник";
        case Role::Medtech:   return "Медтехник";
        case Role::Media:     return "Медиа";
        case Role::Lawman:    return "Законник";
        case Role::Corp:      return "Корпорат";
        case Role::Fixer:     return "Фиксер";
        case Role::Nomad:     return "Кочевник";
        default:              return "Неизвестно";
    }
}
