#pragma once
#include "raylib.h"

// Математика для вида СВЕРХУ (top-down) — гораздо проще, чем изометрия:
// клетка сетки (gx, gy) превращается в пиксели простым умножением на размер
// тайла, без поворота на 45 градусов. "Глубина"/порядок отрисовки тут не нужен:
// тайлы не перекрывают друг друга, поэтому можно рисовать их в любом порядке
// (просто построчно), а не по антидиагоналям, как было в изометрии.
namespace World
{
    // Размер одного тайла в пикселях — подобран под ассеты в resources/Tiles
    // (там все PNG именно 16x16). Если поменяешь тайлсет на другой размер,
    // поменяй только это число — всё остальное пересчитается само.
    constexpr int TILE_SIZE = 16;

    // Переводит координаты клетки в экранные координаты её левого верхнего угла.
    // origin — это "камера": сдвиг всей сцены (например, чтобы центрировать игрока).
    inline Vector2 GridToScreen(int gx, int gy, Vector2 origin)
    {
        return Vector2{
            origin.x + gx * (float)TILE_SIZE,
            origin.y + gy * (float)TILE_SIZE
        };
    }
}
