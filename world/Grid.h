#pragma once
#include "Tile.h"
#include <vector>
#include <string>

// Тактическая сетка — хранит карту как одномерный массив тайлов (width * height)
// и даёт доступ к ним по (x, y). Одномерный массив выбран вместо vector<vector<Tile>>,
// потому что так вся карта лежит одним куском памяти — проще и быстрее для
// такого небольшого прототипа.
//
// Интерфейс класса (InBounds/IsWalkable/At) специально не завязан на то, как именно
// карта была получена — не важно, кем заполнен tiles: процедурным генератором или
// ASCII-текстом, остальной код (main.cpp, отрисовка) работает одинаково в обоих случаях.
class Grid
{
public:
    // Старый способ — просто быстро сгенерировать тестовую прямоугольную карту
    // (рамка стен по периметру + одно жёстко зашитое препятствие). Годится для
    // самой первой проверки, но форму карты руками не покрутишь.
    Grid(int width, int height);

    // Новый способ — карта задаётся текстом-рисунком: список строк одинаковой
    // длины, где '#' означает стену, а любой другой символ (обычно '.') — пол.
    // Пример (см. TestMaps.h):
    //   "#####",
    //   "#...#",
    //   "#####",
    // Так форма карты видна прямо в коде и её легко редактировать руками —
    // не нужно вычислять координаты препятствий формулами.
    explicit Grid(const std::vector<std::string>& rows);

    int GetWidth() const  { return width; }
    int GetHeight() const { return height; }

    // true, если координаты вообще существуют на карте (не вышли за границы).
    bool InBounds(int x, int y) const;

    // true, если по клетке можно ходить: и в границах карты, и это не стена.
    // Именно эту функцию использует main.cpp перед тем, как передвинуть игрока.
    bool IsWalkable(int x, int y) const;

    const Tile& At(int x, int y) const;

private:
    int width;
    int height;
    std::vector<Tile> tiles;
};
