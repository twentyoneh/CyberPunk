#pragma once

// Тип одной клетки карты. Пока их всего два — этого достаточно для прототипа
// движения по сетке. Позже сюда можно добавить, например, укрытия (Cover),
// разрушаемые объекты, ловушки и т.д. — вся логика движения/отрисовки уже
// написана так, что опирается только на IsWalkable(), а не на конкретный enum.
enum class TileType
{
    //добаивть другие тайтлы 
    Floor,
    Wall
};

struct Tile
{
    TileType type = TileType::Floor;

    // Единая точка правды "можно ли сюда встать" — и Grid, и главный цикл
    // спрашивают именно это, а не сравнивают type напрямую.
    bool IsWalkable() const { return type != TileType::Wall; }
};
